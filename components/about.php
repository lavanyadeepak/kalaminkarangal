                          THE AIMS AND OBJECTS OF THE SAID TRUST
1. To promote welfare, social cultural, charitable, health education, humanitarian, and
relief activities that will protect, safeguard, improve the quality and raise the standard
of human life, particularly economically weaker sections of the society, irrespective of
caste creed, race colour, sex or religion so that they may lead healthy, happy contented
and dignified life.
2. To help and collaborate in establishing and running medical centers, old age homes,
Ashrams, rehabilitation, institutions for the destitute, vocational training centers for the
handicapped and self job-oriented activities for the unemployed.
3. To offer scholarships, stipends and financial support to deserving students in
pursuing higher studies in medicine Art, science, Engineering and computer technology.
4. To undertake and support any program or projects that provide educational,
recreational, social cultural and spiritual activities with the sole purpose of promoting
peace, love, tolerance and national integration among the various communities of the
country and Nationalities of the world.
5. Sponsorship of economically weak persons for institutional care.
6. Collaboration with other voluntary organizations to accomplish the above objects.
7. Affiliation with other national and international organizations engaged in welfare,
health education, National integration, and peace-promotion activities.
8. Regular dialogue and rapport with central, State Government and N.G.O’s to elicit
support and cooperation in fulfilling the aims and objects of the organization
(Charitable Trust ).
9. To acquire immovable and movable properties by purchase lease, mortgage, gifts,
grants, legacy, bequest, exchange, right privilege from any person/s, company, society,
Government cooperative bodies or institutions whatsoever and to hold movable and
immovable properties lawfully held of all descriptions.
10. To erect/build residential complexes, hostels, hospitals and any other type of
building or structures whatsoever, to further any objects of the Charitable Trust and to
maintain, manage, control, improve, alter, repairs, demolish or reconstruct the same or
any portion or portions thereof.
11. To receive by way of gift, whether in cash money or property movable or
immovable, donations and contributions in any legal form, including legacy, behest,
wills, trusteeship, etc, for the benefit of the Charitable Trust.
12. To alienate by way of sale, mortgage, lease, charge transfer all or any part of the
property of the charitable Trust.
13. To invest and re-invest any funds or moneys of the Charitable Trust in the interest of
the beneficiaries of the Trust.
14. To execute such agreements, conveyances, transfers, leases licence, mortgages,
documents, etc. as may be deemed necessary to carry out the objects of the Charitable
Trust.
15. To borrow and or raise any funds with or without security in any manner the
charitable trust may think to repay the same.
16. To negotiate with or enter into arrangements with different authorities, bodies,
