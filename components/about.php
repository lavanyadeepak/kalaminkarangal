                  <h3>Target Beneficiaries</h3>
                  <div>
               Kalamin Karangal began as a Trust that pioneered in rejuvenating underprivileged slums and vulnerable neighborhoods into self-sustaining and developed communities by its Integrated Community Development Program changing the environment of urban slums. Kalamin Karangal through its one-stop centers pro-vides supports to slum dwellers to obtain government identities, providing them access to public benefit schemes, imparting skills leading to employment, micro-finance for vital personal consumption and setting up SMEs and providing access to habitable environment and basic education.       
