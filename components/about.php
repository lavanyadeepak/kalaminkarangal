                         <h3>Mission</h3>
                         Kalamin Karangal envisions inclusive and empowered communities and individuals.
                         <h3>Vision</h3>
                         To make human settlements equitable living environments where all residents and vulnerable people have access to health, education, essential infrastructure services and livelihood options, irrespective of their economic and social status.
                     </div>
                     <div id="vision">

                     </div>
                     <div id="approach">
                         Kalamin Karangal engages organizations, corporates and individuals from India and globally as partners and supporters. Kalamin Karangal initiated the Integrated Community Development Programme, an undertaking that seeks to rejuvenate slums into vibrant neighborhoods. Kalamin Karangal works with slum residents, children, women, youth and vulnerable people in urban and rural areas. It addresses multiple needs of the socio-economically vulnerable with one-stop solutions, through which slum residents have access to basic services for holistic growth. Kalamin Karangal collaborates with the communities, specially the youth to undertake their upliftment programs. Communities co-invest with Kalamin Karangal and donors for the program implementation and scaling-up.

                     </div>
                     <div id="theoryofchange">
                         By utilizing Kalamin Karangal’s 2 years of experience of working with the communities, its market and need based innovative approach within multiple sectors, its reach and engaging with the stakeholders. Kalamin Karangal will be able to empower thousands of vulnerable families by December 2020, by working for their livelihoods, financial inclusion, community leadership and education. To be successful at this Kalamin Karangal will have to build the capacity of its team members and community leaders, use technology for bringing quality, cost-effectiveness monitoring, bring accountability, expand its current reach, leverage interlinkages and bring in more funds by taking new approach and tapping new sources.
However, with the active support from kindhearted individuals and organisations, the Trust has evolved itself into a movement of rural youth and taken up the responsibility of rural areas in larger perspective.
